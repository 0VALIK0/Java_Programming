package day16continueLoop;

public class LoopPractices {
    public static void main(String[] args) {


        for (int i = 0; i < 6; i++) {
            System.out.println("Hello "+i);

        }

        for (int i = 0; i < 6; i++) {
            System.out.println("Hello ++"+i);}

        System.out.println("**********************************");
        int k = 0;
        do {
            System.out.println("He he he he");
            k++;
        }while (k<5);

    }
}
